package com.objectivasoftware.BCIA.model;

public class Park {
	private boolean isSelect;
	private String parkValue;
	/**
	 * @return the isSelect
	 */
	public boolean isSelect() {
		return isSelect;
	}
	/**
	 * @param isSelect the isSelect to set
	 */
	public void setSelect(boolean isSelect) {
		this.isSelect = isSelect;
	}
	/**
	 * @return the parkValue
	 */
	public String getParkValue() {
		return parkValue;
	}
	/**
	 * @param parkValue the parkValue to set
	 */
	public void setParkValue(String parkValue) {
		this.parkValue = parkValue;
	}
}
